  const LANGS = { es: ES, en: EN };


  // ── ESTADO ───────────────────────────────────────────────────
  let _lang = localStorage.getItem('clarokpis_lang') || 'es';

  // ── FUNCIONES ────────────────────────────────────────────────
  function t(key, vars) {
    let val = LANGS[_lang]?.[key] ?? LANGS['es']?.[key] ?? key;
    // Interpolación: {nombre}, {pct}, etc.
    if (vars && typeof val === 'string') {
      Object.entries(vars).forEach(([k, v]) => {
        val = val.replace(new RegExp(`{${k}}`, 'g'), v);
      });
    }
    return val;
  }

  function getLang()  { return _lang; }

  function setLang(lang) {
    if (!LANGS[lang]) return;
    _lang = lang;
    localStorage.setItem('clarokpis_lang', lang);
    applyToDOM();
    document.dispatchEvent(new CustomEvent('clarokpis:langChange', { detail: { lang } }));
  }

  function toggle() { setLang(_lang === 'es' ? 'en' : 'es'); }

  function applyToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      if (key) el.title = t(key);
    });
    const langBtns = document.querySelectorAll('#langToggleNav, #langToggleHeader');
    langBtns.forEach(btn => {
      if (btn) btn.textContent = _lang === 'es' ? '🌐 EN' : '🌐 ES';
    });
  }

  function monthName(monthIndex) {
    const keys = ['monthJan','monthFeb','monthMar','monthApr','monthMay','monthJun',
                  'monthJul','monthAug','monthSep','monthOct','monthNov','monthDec'];
    return t(keys[monthIndex] ?? 'monthJan');
  }

  function formatMonthYear(date) {
    if (!date) return '—';
    const d = date instanceof Date ? date : new Date(date);
    return `${monthName(d.getMonth())} ${d.getFullYear()}`;
  }

  // Formatear delta para KPI cards
  function formatDelta(pct, compareMode) {
    if (pct === null || pct === undefined) return null;
    const suffix = compareMode === 'lastyear' ? t('deltaVsLastYear') : t('deltaVsPrev');
    const sign   = pct >= 0 ? '+' : '';
    const color  = pct >= 0 ? 'var(--color-green)' : 'var(--color-red)';
    const arrow  = pct >= 0 ? '↑' : '↓';
    return {
      text:   `${arrow} ${sign}${pct.toFixed(1)}% ${suffix}`,
      color,
      pct,
    };
  }

  return {
    t,
    getLang,
    setLang,
    toggle,
    applyToDOM,
    monthName,
    formatMonthYear,
    formatDelta,
    LANGS,
  };
